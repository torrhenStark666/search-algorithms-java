    private void jButtonIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniciarActionPerformed
        if(this.jTFDestino.getText() == null)
            this.jTFDestino.setText("9x9");
        
        if(this.botaoAestrela.isSelected()){
            controllerTela.iniciarBusca(this.jTFDestino.getText());

        }else if (this.botaoLargura.isSelected()){
            try {
                controllerTela.iniciarBuscaLargura(this.jTFDestino.getText());
            } catch (InterruptedException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if( this.botaoProfundidade.isSelected()){
            controllerTela.iniciarBuscaProfundidade(this.jTFDestino.getText());
        }else if( this.botaoGulosa.isSelected()){
            controllerTela.iniciarBuscaGulosa(this.jTFDestino.getText());
        }
  
       
    }//GEN-LAST:event_jButtonIniciarActionPerformed

